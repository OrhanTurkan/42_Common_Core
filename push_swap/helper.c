#include "push_swap.h"

void	three(t_p *p)
{
	if ((p->b[1] > p->b[0]) && (p->b[1] > p->b[2]) \
		&& (p->b[0] < p->b[2]))
		rb(p);
	else if ((p->b[1] > p->b[0]) && (p->b[1] > p->b[2]) \
		&& (p->b[0] > p->b[2]))
		sb(p);
	else if ((p->b[1] < p->b[0]) && (p->b[1] < p->b[2]) \
		&& (p->b[0] < p->b[2]))
		rrb(p);
	else if ((p->b[1] < p->b[0]) && (p->b[1] < p->b[2]) \
		&& (p->b[0] > p->b[2]))
	{
		sb(p);
		rb(p);
	}
	else if ((p->b[1] > p->b[0]) && (p->b[1] < p->b[2]) \
		&& (p->b[0] < p->b[2]))
	{
		sb(p);
		rrb(p);
	}
	if (p->a[0] > p->a[1])
		sa(p);
}

void	two(t_p *p)
{
	if (p->b[0] < p->b[1])
		rb(p);
	if (p->a[0] > p->a[1])
		ra(p);
}
