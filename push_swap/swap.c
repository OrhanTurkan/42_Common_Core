#include "push_swap.h"

void	sa(t_p *p)
{
	int	temp;

	if (p->sizea >= 2)
	{
		temp = p->a[1];
		p->a[1] = p->a[0];
		p->a[0] = temp;
	}
	write(1, "sa\n", 3);
}

void	sb(t_p *p)
{
	int	temp;

	if (p->sizeb >= 2)
	{
		temp = p->b[1];
		p->b[1] = p->b[0];
		p->b[0] = temp;
	}
	write(1, "sb\n", 3);
}
